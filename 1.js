$(document).ready(function(){
    $(".more").click(function(){
        $(".p2").slideDown();
        $(".more").hide();
        $(".less").show();
    });
    $(".less").click(function(){
        $(".p2").slideUp();
        $(".more").show();
        $(".less").hide();
    })
});
